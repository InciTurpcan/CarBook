using CarBook_Application.Features.Mediator.Queries.StatisticsQueries;
using CarBook_Application.Features.Mediator.Results.StatisticsResults;
using CarBook_Application.Interfaces;
using CarBook_Application.Interfaces.CarInterfaces;
using CarBook_Domain.Entities;
using MediatR;
using Microsoft.VisualBasic;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CarBook_Application.Features.Mediator.Handlers.StatisticsHandlers
{
    public class GetCarCountQueryHandler : IRequestHandler<GetCarCountQuery, GetCarCountQueryResult>
    {
        private readonly ICarRepository _repository;

        public GetCarCountQueryHandler(ICarRepository carRepository)
        {
            _repository = carRepository;
        }

        public async Task<GetCarCountQueryResult> Handle(GetCarCountQuery request, CancellationToken cancellationToken)
        {
            var value = _repository.GetCarCount();
            return new GetCarCountQueryResult
            {
                CarCount= value,
            };
        }
    }
}
